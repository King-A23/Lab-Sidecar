def read_report_summary(workspace: Path, task_id: str) -> dict[str, Any]:
    path = workspace / ".lab-sidecar" / "tasks" / task_id / "reports" / "report-summary.json"
    return json.loads(path.read_text(encoding="utf-8"))


def assert_report_summary_contract(
    summary: dict[str, Any],
    *,
    workspace: Path,
    expected_task_id: str,
    expected_status: str,
) -> None:
    task_path = workspace / ".lab-sidecar" / "tasks" / expected_task_id
    reports_dir = task_path / "reports"

    assert REQUIRED_TOP_LEVEL_KEYS <= set(summary)
    assert summary["schema_version"] == "1"
    assert summary["task_id"] == expected_task_id
    assert isinstance(summary["template"], str) and summary["template"]
    assert isinstance(summary["generated_at"], str) and summary["generated_at"]
    assert summary["report_path"] == "reports/report-fragment.md"
    assert summary["summary_path"] == "reports/report-summary.json"
    assert (task_path / "reports" / "report-fragment.md").is_file()
    assert (task_path / "reports" / "report-summary.json").is_file()

    _assert_source_artifacts(summary["generated_from"], task_path=task_path)
    _assert_source_artifacts(summary["source_artifacts"], task_path=task_path)
    assert summary["generated_from"] == summary["source_artifacts"]

    _assert_provenance(summary["provenance"], expected_task_id=expected_task_id, expected_status=expected_status)
    _assert_metrics_summary(summary["metrics"], task_path=task_path)
    _assert_figures_summary(summary["figures"], task_path=task_path, reports_dir=reports_dir, expected_task_id=expected_task_id)
    _assert_failure_context(summary["failure"], task_path=task_path)
    _assert_cancellation_context(summary["cancellation"], task_path=task_path)
    _assert_reproduce(summary["reproduce"], task_path=task_path)
    _assert_claim_traces(summary["claim_traces"], summary=summary, expected_status=expected_status)
    _assert_summary_is_reference_only(summary)


def _assert_source_artifacts(items: list[str], *, task_path: Path) -> None:
    assert isinstance(items, list)
    assert items
    assert "reports/report-fragment.md" not in items
    assert "reports/report-summary.json" not in items
    for item in items:
        assert isinstance(item, str) and item and "\n" not in item
        assert (task_path / item).exists()


def _assert_provenance(
    provenance: dict[str, Any],
    *,
    expected_task_id: str,
    expected_status: str,
) -> None:
    assert set(provenance) == REQUIRED_PROVENANCE_KEYS
    assert provenance["task_id"] == expected_task_id
    assert provenance["status"] == expected_status
    assert provenance["status"] in TASK_STATUS_VALUES
    assert provenance["mode"] in {"run", "ingest"}
    for key in ["command", "source_path", "working_dir", "created_at", "started_at", "finished_at"]:
        assert isinstance(provenance[key], str) and provenance[key]
    assert provenance["exit_code"] == "未自动推断" or isinstance(provenance["exit_code"], int)


def _assert_metrics_summary(metrics: dict[str, Any], *, task_path: Path) -> None:
    assert REQUIRED_METRICS_KEYS <= set(metrics)
    assert isinstance(metrics["present"], bool)
    assert metrics["path"] == "metrics/normalized_metrics.csv"
    assert metrics["collection_summary_path"] in {None, "metrics/collection-summary.json"}
    assert metrics["scenario_summary_path"] in {None, "metrics/scenario-summary.json"}

    if metrics["collection_summary_path"] is not None:
        assert (task_path / metrics["collection_summary_path"]).is_file()
    if metrics["scenario_summary_path"] is not None:
        assert (task_path / metrics["scenario_summary_path"]).is_file()

    row_count = metrics["row_count"]
    assert isinstance(row_count, int)
    assert row_count >= 0

    columns = metrics["columns"]
    displayed_columns = metrics["displayed_columns"]
    assert isinstance(columns, list)
    assert all(isinstance(column, str) and column for column in columns)
    assert isinstance(displayed_columns, list)
    assert displayed_columns == columns[:MAX_DISPLAY_COLUMNS]
    assert metrics["omitted_column_count"] == max(0, len(columns) - MAX_DISPLAY_COLUMNS)

    numeric_summaries = metrics["numeric_summaries"]
    assert isinstance(numeric_summaries, list)
    assert len(numeric_summaries) <= MAX_NUMERIC_SUMMARY_COLUMNS
    for item in numeric_summaries:
        _assert_numeric_summary(item, columns=columns, row_count=row_count)
    if metrics["numeric_omitted_count"] > 0:
        assert len(numeric_summaries) == MAX_NUMERIC_SUMMARY_COLUMNS
    else:
        assert len(numeric_summaries) <= MAX_NUMERIC_SUMMARY_COLUMNS
    assert isinstance(metrics["numeric_omitted_count"], int)
    assert metrics["numeric_omitted_count"] >= 0

    detected_fields = metrics["detected_fields"]
    assert isinstance(detected_fields, list)
    assert all(isinstance(field, str) and field for field in detected_fields)
    assert len(detected_fields) == len(set(detected_fields))

    if metrics["present"]:
        assert row_count > 0
        assert (task_path / metrics["path"]).is_file()
        assert columns
        if "candidate_count" in metrics:
            assert isinstance(metrics["candidate_count"], int)
            assert metrics["candidate_count"] >= 0
        if "processed_files" in metrics:
            assert isinstance(metrics["processed_files"], list)
            assert all(isinstance(item, dict) for item in metrics["processed_files"])
    else:
        assert row_count == 0
        assert columns == []
        assert displayed_columns == []
        assert metrics["omitted_column_count"] == 0
        assert numeric_summaries == []
        assert metrics["numeric_omitted_count"] == 0
        assert detected_fields == []

    _assert_compact_scenario(metrics["scenario"])


def _assert_numeric_summary(item: dict[str, Any], *, columns: list[str], row_count: int) -> None:
    assert set(item) == {"column", "count", "mean", "min", "max"}
    assert isinstance(item["column"], str) and item["column"]
    assert item["column"] in columns
    assert isinstance(item["count"], int)
    assert 0 < item["count"] <= row_count
    for key in ["mean", "min", "max"]:
        assert item[key] is None or isinstance(item[key], int | float)


def _assert_compact_scenario(scenario: dict[str, Any]) -> None:
    assert isinstance(scenario, dict)
    if not scenario.get("present"):
        assert scenario == {"present": False}
        return

    assert REQUIRED_SCENARIO_KEYS <= set(scenario)
    assert scenario["path"] == "metrics/scenario-summary.json"
    assert scenario["scenario_type"] in {"training-run", "algorithm-benchmark"}

    primary_metric = scenario["primary_metric"]
    assert set(primary_metric) == REQUIRED_PRIMARY_METRIC_KEYS
    assert primary_metric["name"] is None or isinstance(primary_metric["name"], str)
    assert primary_metric["direction"] in {"max", "min", None}
    assert primary_metric["unit"] is None or _is_bounded_scalar(primary_metric["unit"])
    assert _is_bounded_scalar(primary_metric["selection_reason"])

    groups = scenario["groups"]
    assert set(groups) == REQUIRED_GROUP_KEYS
    assert isinstance(groups["configured"], dict)
    assert groups["primary"] is None or isinstance(groups["primary"], str)
    assert groups["secondary"] is None or isinstance(groups["secondary"], str)
    assert groups["seed"] is None or isinstance(groups["seed"], str)
    assert isinstance(groups["context"], list)
    assert isinstance(groups["inferred"], list)

    best_rows = scenario["best_rows"]
    last_rows = scenario["last_rows"]
    assert isinstance(best_rows, list)
    assert isinstance(last_rows, list)
    assert len(best_rows) <= 3
    assert len(last_rows) <= 3
    for item in best_rows:
        _assert_compact_best_row(item)
    for item in last_rows:
        _assert_compact_last_row(item)

    seed_aggregates = scenario["seed_aggregates"]
    assert isinstance(seed_aggregates["present"], bool)
    assert len(seed_aggregates.get("items", [])) <= 3
    if seed_aggregates["present"]:
        assert isinstance(seed_aggregates["metric"], str) and seed_aggregates["metric"]
        assert seed_aggregates["direction"] in {"max", "min"}
        assert isinstance(seed_aggregates["claim_limit"], str) and seed_aggregates["claim_limit"]
        for item in seed_aggregates["items"]:
            _assert_compact_seed_aggregate(item)
    else:
        assert seed_aggregates["items"] == []

    warnings = scenario["warnings"]
    assert isinstance(warnings, list)
    assert len(warnings) <= 5
    assert all(isinstance(item, str) and item for item in warnings)

    omitted = scenario["omitted"]
    assert isinstance(omitted, dict)
    for value in omitted.values():
        assert value == "omitted_by_default"


