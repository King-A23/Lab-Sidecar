def _assert_claim_traces(
    traces: list[dict[str, Any]],
    *,
    summary: dict[str, Any],
    expected_status: str,
) -> None:
    assert isinstance(traces, list)
    assert traces

    by_id = {item["claim_id"]: item for item in traces}
    for item in traces:
        assert {"claim_id", "surface", "claim_type", "value", "evidence"} <= set(item)
        assert item["surface"] == "report"
        assert isinstance(item["claim_id"], str) and item["claim_id"]
        assert isinstance(item["claim_type"], str) and item["claim_type"]
        assert isinstance(item["evidence"], list)
        assert item["evidence"]
        for evidence in item["evidence"]:
            _assert_claim_evidence(evidence)

    figures = summary["figures"]
    assert by_id["report.figures.count"]["claim_type"] == "figure_count"
    assert by_id["report.figures.count"]["value"] == figures["figure_count"]

    metrics = summary["metrics"]
    if metrics["present"]:
        assert "report.metrics.row_count" in by_id
        assert by_id["report.metrics.row_count"]["value"] == metrics["row_count"]
        assert "report.metrics.detected_fields" in by_id
        assert by_id["report.metrics.detected_fields"]["value"] == metrics["detected_fields"]

        for item in metrics["numeric_summaries"]:
            for operation in ["mean", "min", "max"]:
                claim_id = f"report.metric.{item['column']}.{operation}"
                assert claim_id in by_id
                assert by_id[claim_id]["claim_type"] == "numeric_summary"
                assert by_id[claim_id]["operation"] == operation
                assert by_id[claim_id]["field"] == item["column"]

        if metrics["numeric_omitted_count"] > 0:
            assert "report.metrics.numeric_fields_omitted" in by_id
            assert by_id["report.metrics.numeric_fields_omitted"]["value"] == metrics["numeric_omitted_count"]

        scenario = metrics["scenario"]
        if scenario["present"]:
            assert "report.metrics.scenario_summary" in by_id
            evidence = by_id["report.metrics.scenario_summary"]["evidence"]
            assert evidence[0]["artifact_id"] == "metrics_scenario_summary"
            assert evidence[0]["body"] == "omitted"
    else:
        assert "report.metrics.unavailable" in by_id
        assert by_id["report.metrics.unavailable"]["claim_type"] == "unknown_or_unavailable"
        assert not any(item["claim_type"] == "numeric_summary" for item in traces)

    if expected_status == "failed":
        assert "report.diagnostic.failed_status" in by_id
        assert by_id["report.diagnostic.failed_status"]["value"] == "failed"
    elif expected_status == "cancelled":
        assert "report.diagnostic.cancelled_status" in by_id
        assert by_id["report.diagnostic.cancelled_status"]["value"] == "cancelled"
    else:
        assert "report.diagnostic.failed_status" not in by_id
        assert "report.diagnostic.cancelled_status" not in by_id


def _assert_claim_evidence(evidence: dict[str, Any]) -> None:
    assert isinstance(evidence, dict)
    assert isinstance(evidence.get("artifact_id"), str) and evidence["artifact_id"]
    assert isinstance(evidence.get("path"), str) and evidence["path"]
    assert "rows" not in evidence
    if evidence["artifact_id"] in {"metrics_scenario_summary", "log_stderr"}:
        assert evidence["body"] == "omitted"
    elif "body" in evidence:
        assert evidence["body"] == "omitted"


def _assert_summary_is_reference_only(summary: dict[str, Any]) -> None:
    serialized = json.dumps(summary, ensure_ascii=False)
    for fragment in FORBIDDEN_EMBEDDED_FRAGMENTS:
        assert fragment not in serialized


def _is_bounded_scalar(value: Any) -> bool:
    if isinstance(value, str):
        return len(value) <= 160
    return value is None or isinstance(value, bool | int | float)


def _write_wide_metrics_csv(path: Path) -> None:
    headers = [
        "epoch",
        "model",
        "seed",
        "val_accuracy",
        "val_loss",
        *[f"metric_{index:02d}" for index in range(1, 11)],
        *[f"note_{index:02d}" for index in range(1, 11)],
    ]
    rows = [
        [
            "1",
            "wide-a",
            "1",
            "0.71",
            "0.52",
            *[str(index) for index in range(1, 11)],
            *[f"note-a-{index}" for index in range(1, 11)],
        ],
        [
            "2",
            "wide-a",
            "1",
            "0.82",
            "0.41",
            *[str(index + 1) for index in range(1, 11)],
            *[f"note-b-{index}" for index in range(1, 11)],
        ],
    ]
    text = "\n".join([",".join(headers), *(",".join(row) for row in rows)]) + "\n"
    path.write_text(text, encoding="utf-8")


